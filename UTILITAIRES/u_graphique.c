void t_chaine_centree(const char *s)
{
  GLubyte *b ;
  int i, j, n, i_last ;

  glNormal3f(0,0,1) ;
  glPushMatrix() ;
  glScalef(1/9., 1/9., 1) ;
  glTranslatef(-(strlen(s)*9.)/2., -7, 0) ;
  n = 0 ;

  glBegin(GL_QUADS) ;
  for( ; *s; s++)
    {
      if ( *s >=' ' )
	{
	  b = rasters[*s - ' '] ;
	  for(j=0; j<13; j++)
	    {
	      for(i=0;;)
		{
		  for(;  (b[j] & (128>>i))==0 && i<8 ; i++)
		    {
		    }
		  if ( i==8 )
		    break ;
		
		  for(i_last=i+1 ; (b[j]&(128>>i_last)) && i_last<8;i_last++ )
		    {
		    }
		  glVertex2i(n+i, j) ;
		  glVertex2i(n+i_last, j) ;
		  glVertex2i(n+i_last, j+1) ;
		  glVertex2i(n+i, j+1) ;
		  i = i_last ;
		}
	      }
	  n += 9 ;
	}
    }
  glEnd() ;
  glPopMatrix() ;
}
