void u_multiplie_epaisseur_ligne(float v)
{
  GLfloat w ;

  glGetFloatv(GL_LINE_WIDTH, &w) ;
  if ( w == 0 )
    w = 1 ;
  glLineWidth(v+w) ;
}

void u_multiplie_taille_point(float v)
{
  GLfloat w ;

  glGetFloatv(GL_POINT_SIZE, &w) ;
  if ( w == 0 )
    w = 1 ;
  glPointSize(v+w) ;
}

